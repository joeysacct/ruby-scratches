  def self.decrypt_monoalpha(ciphertext, key)
    alphabet = ('a'..'z').to_a

    plaintext = []
    ciphertext.each_char do |chr|
      plaintext << key[alphabet.index(chr)]
    end
    plaintext.join
  end

  def self.get_digram_matrix(text)
    alphabet_length = 26
    digram_matrix = Array.new(alphabet_length) { Array.new(alphabet_length) {0} }
    text = text.downcase

    digrams = 0
    for i in (0..text.length-1) do
      char1 = text[i]
      char2 = text[i+1]
      next unless Alphabet.chars.include?(char1) && Alphabet.chars.include?(char2)
      a = Freq_order.index(char1)
      b = Freq_order.index(char2)

      digram_matrix[a][b] += 1
      digrams += 1
    end

    #normalize based on num digrams
    for i in (0..alphabet_length-1) do
      for j in (0..alphabet_length-1) do
        digram_matrix[i][j] = 100 * digram_matrix[i][j].to_f / digrams
      end
    end
    # raise digram_matrix.inspect

    return digram_matrix
  end

  def self.swap_matrix(mat,i1,i2)
    matrix = mat
    return matrix if i1 == i2
    # Swap rows
    matrix[i1], matrix[i2] = matrix[i2], matrix[i1]

    # Swap columns in each row
    matrix.each do |row|
      row[i1], row[i2] = row[i2], row[i1]
    end
    return matrix
  end

  def self.get_expected_key(ciphertext)
    cipher_char_freqs = {}
    ('a'..'z').to_a.each do |ltr|
      cipher_char_freqs[ltr] = ciphertext.chars.count{|chr| chr == ltr}
    end
    sorted_freqs = cipher_char_freqs.sort_by{|k,v| -v}
    key = []
    # puts sorted_freqs.inspect
    Alphabet.each_char do |chr|
      key << [sorted_freqs[Freq_order.index(chr)][0], chr]
      # puts key.inspect
    end
    # puts key.inspect
    key = key.sort_by{|n| n[0] }
    # puts key.inspect

    return key.map{|n| n[1]}.join
  end

  def self.get_freq_list(ciphertext)
    cipher_char_freqs = {}
    ('a'..'z').to_a.each do |ltr|
      cipher_char_freqs[ltr] = ciphertext.chars.count{|chr| chr == ltr}.to_f / ciphertext.length
    end
    cipher_char_freqs.map{|k,v| v}
  end

